import { poland } from '../scripts/poland.js';

test('2 + 3 to equal 23+', () => {
  expect(poland(["2", "+", "3"])).toStrictEqual(["2", "3", "+"]);
})

test('(2+3)\u00D75-(4+2) to equal 23+5\u00D742+-', () => {
  expect(poland(["(", "2", "+", "3", ")", "\u00D7", "5", "-", "(", "4", "+", "2", ")"])).toStrictEqual(["2", "3", "+", "5", "\u00D7", "4", "2", "+", "-"]);
})

test('(2+9)^4 to equal 29+11^', () => {
  expect(poland(["(", "2", "+", "9", ")", "^", "11"])).toStrictEqual(["2", "9", "+", "11", "^"]);
})

test('\u221A(3+2^14) to equal 3214^+\u221A', () => {
  expect(poland(["\u221A","(", "3", "+", "2", "^", "14", ")"])).toStrictEqual(["3", "2", "14", "^", "+", "\u221A"]);
})

test('4^(14-2^(4-2))/15 to equal 414242-^-^15/', () => {
  expect(poland(["4", "^", "(", "14", "-", "2", "^", "(", "4", "-", "2", ")", ")", "/", "15"])).toStrictEqual(["4", "14", "2", "4", "2", "-", "^", "-", "^", "15", "/"]);
})

test('\u221B(2+5^2) to equal 252^+\u221B', () => {
  expect(poland(["\u221B", "(", "2", "+", "5", "^", "2", ")"])).toStrictEqual(["2", "5", "2", "^", "+", "\u221B"]);
})

test('1/(5^2)\u00D72/21 to equal 152^/2*21/', () => {
  expect(poland(["1", "/", "(", "5", "^", "2", ")", "\u00D7", "2", "/", "21"])).toStrictEqual(["1", "5", "2", "^", "/", "2", "\u00D7", "21", "/"]);
})